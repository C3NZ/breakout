class Ball {
    constructor() {
        this.x = x;
        this.y = y;
        this.dx = 2;
        this.dy = 2;
    }

    get x() {
        return this.x;
    }

    set x(newX) {
        this.x = newX;
    }

    get y() {
        return this.y;
    }

    set y(newY) {
        this.y = newY;
    }

    get dx() {
        return this.dx;
    }

    set dx(newDx) {
        this.dx = newDx;
    }

    get dy() {
        return this.dy;
    }

    set dy(newDy) {
        this.dy = newDy; 
    }
}
